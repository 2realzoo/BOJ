import sys

input = sys.stdin.readline

# d[1] = [1] -> 1
# d[2] = [1,1], [2] -> 2 d[1] + 1
# d[3] = [1,1,1],[1,2],[2,1],[3] -> 4 1 + 2 + [3]
# d[4] = [1,1,1,1],[1,1,2], [1,2,1],[2,1,1],[1,3][3,1],[2,2]->7 1 + 2 + 3 + 1
# d[5] = [1,1,1,1,1], [1,1,1,2], [1,1,2,1],[1,2,1,1], [2,1,1,1], [1,1,3],[1,3,1],[3,1,1],[2,2,1],[2,1,2],[1,2,2],[2,3],[3,2] ->1+3+4+3+2 
# d[n] = 1 + d[n-1].append(1) + d[n-2].append(2) + d[n-3].append(3)
 
T = int(input())
d = {1:[[1]], 2:[[1,1],[2]], 3:[[1,1,1],[1,2],[2,1],[3]]}
for _ in range(T):
    N = int(input())
    for n in range(1, N+1):
        if not n in d:
            d[n] = []
            for i in d[n-1]:
                d[n].append([*i,1])
            for i in d[n-2]:
                d[n].append([*i,2])
            for i in d[n-3]:
                d[n].append([*i,3])

    print(len(d[N]))